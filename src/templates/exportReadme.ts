import type { OrchestrationConfig } from '../core/types';

export function generateExportReadme(
  config: OrchestrationConfig,
  platform: string
): string {
  return `# 🎲 Agent Party: ${config.questName}

This package contains your customized Agent Orchestration setup, exported for **${platform}**.

## 🏗️ The 3-Tier Hierarchy

1.  **The War Room (Meta-Level):** Strategic planning and rule-setting. Runs in this directory.
2.  **The GM Session (Coordinator):** Project management and coordination. Runs inside your target codebase.
3.  **The Party (Sub-Agents):** Specialized workers (Ranger, Wizard, etc.) spawned by the GM.

## 🚀 Getting Started

### 1. Initialize the War Room
Start your meta-session in this directory to brainstorm the quest and refine rules.
\`\`\`bash
# Using the provided helper script:
chmod +x start-war-room.sh
./start-war-room.sh
\`\`\`

### 2. Launch the Quest
Navigate to your **target codebase repository** and run the quest session.
\`\`\`bash
# From inside your project's root directory:
/path/to/this/folder/start-quest.sh .
\`\`\`

## 🛠️ Customization

If you are using a different AI agent than the one selected during export, you may need to:
- Adjust the CLI commands in \`start-war-room.sh\` and \`start-quest.sh\`.
- Modify the file paths referenced in the GM Guide to match your agent's directory structure.

## 📂 Folder Structure
- \`war-room.md\`: Meta-level instructions.
- \`gm-guide.md\`: The main coordinator prompt.
- \`party/\`: Prompts for your specialized sub-agents.

Generated by [Agent Party Planner](https://agentsparty.dev)
`;
}
